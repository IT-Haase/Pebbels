internal fun hexToBytes(s: String): ByteArray {
    val t = if (s.length % 2 == 1) "0$s" else s
    return ByteArray(t.length / 2) { ((t[it * 2].digitToInt(16) shl 4) or t[it * 2 + 1].digitToInt(16)).toByte() }
}
internal fun bytesToHex(b: ByteArray): String =
    b.joinToString("") { ((it.toInt() and 0xff) + 0x100).toString(16).substring(1) }

